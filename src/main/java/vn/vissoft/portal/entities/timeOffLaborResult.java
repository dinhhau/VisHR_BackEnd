public class timeOffLaborResult {

    private Integer userId;

    private Integer toTimeOff;

    private Integer toLabor;

    private Integer toTimeOffLeft;

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getToTimeOff() {
        return toTimeOff;
    }

    public void setToTimeOff(Integer toTimeOff) {
        this.toTimeOff = toTimeOff;
    }

    public Integer getToLabor() {
        return toLabor;
    }

    public void setToLabor(Integer toLabor) {
        this.toLabor = toLabor;
    }

    public Integer getToTimeOffLeft() {
        return toTimeOffLeft;
    }

    public void setToTimeOffLeft(Integer toTimeOffLeft) {
        this.toTimeOffLeft = toTimeOffLeft;
    }
}

